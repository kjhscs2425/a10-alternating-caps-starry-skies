
def alt_caps():
    text = text = input("What sentence do you want me to turn into alternating caps?")
    text = text.replace("a", "A")
    text = text.replace("e", "E")
    text = text.replace("i", "I")
    text = text.replace("o", "O")
    text = text.replace("u", "U")
    print(text)
    return
alt_caps()